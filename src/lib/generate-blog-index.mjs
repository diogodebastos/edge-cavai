#!/usr/bin/env node
import fs from "fs";
import path from "path";

const blogDir = path.join(process.cwd(), "src", "content", "blog");
const outFile = path.join(blogDir, "index.ts");

if (!fs.existsSync(blogDir)) {
  console.error("Blog directory not found:", blogDir);
  process.exit(1);
}

const files = fs
  .readdirSync(blogDir)
  .filter((f) => f.endsWith(".md"))
  .sort((a, b) => {
    const na = Number((a.match(/\d+/g) || []).slice(-1)[0] || 0);
    const nb = Number((b.match(/\d+/g) || []).slice(-1)[0] || 0);
    return nb - na; // descending numeric
  });

const importLines = [];
const mappingLines = [];

files.forEach((file) => {
  const base = path.basename(file, ".md");
  const varName = "bp_" + base.replace(/[^a-zA-Z0-9_]/g, "_");
  importLines.push(`import ${varName} from './${file}';`);
  const slug = base.replace(/_/g, "-");
  mappingLines.push(`  "${slug}": ${varName},`);
});

const content = `/* This file is generated by src/lib/generate-blog-index.mjs — do not edit. */\n${importLines.join('\n')}\n\nconst posts: Record<string, string> = {\n${mappingLines.join('\n')}\n};\n\nexport default posts;\n`;

const prev = fs.existsSync(outFile) ? fs.readFileSync(outFile, "utf8") : null;
if (prev === content) {
  console.log("No changes to", outFile);
} else {
  fs.writeFileSync(outFile, content);
  console.log("Wrote", outFile);
}
